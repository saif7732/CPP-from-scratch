#include <iostream>

using namespace std;

int main(){
    
    int a=10, b=3;

    cout << "The sum of a and b is: " << a+b << endl;
    cout << "The sub of a and b is: " << a-b << endl;
    cout << "The div of a and b is: " << (float)a/b << endl;
    cout << "The mul of a and b is: " << a*b << endl;
    cout << "The mod of a and b is: " << a%b << endl;
    cout << "The increment of a is: " << ++a << endl;
    cout << "The increment of b is: " << ++b << endl;

    return 0;
}